package com.drupalpoc.api.model;

import java.util.List;

public class Course {

	private String courseCode;
	private String courseName;
	private String courseProperties;
	private String courseUser;
	private String courseLocation;
	public String getCourseCode() {
		return courseCode;
	}
	public void setCourseCode(String courseCode) {
		this.courseCode = courseCode;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseProperties() {
		return courseProperties;
	}
	public void setCourseProperties(String courseProperties) {
		this.courseProperties = courseProperties;
	}
	public String getCourseUser() {
		return courseUser;
	}
	public void setCourseUser(String courseUser) {
		this.courseUser = courseUser;
	}
	public String getCourseLocation() {
		return courseLocation;
	}
	public void setCourseLocation(String courseLocation) {
		this.courseLocation = courseLocation;
	}
	



	
	
	
}
